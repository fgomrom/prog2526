public class EjemploIncrementos {
    public static void main(String[] args) {
        int a, b, c;
        a = 1;
        b = a++;
        c = ++a;
        
        System.out.println("a: " + a);
        System.out.println("b: " + b);
        System.out.println("c: " + c);
    }
}
